package com.brein.domain;

/**
 * The type of the activity collected, i.e., one of search, login, logout, addToCart,
 * removeFromCart, checkOut, selectProduct, or other.
 */
public enum BreinActivityType {

    SEARCH,
    LOGIN,
    LOGOUT,
    ADD_TO_CART,
    REMOVE_FROM_CART,
    SELECT_PRODUCT,
    LOOKUP,
    OTHER
}

